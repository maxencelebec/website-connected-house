function message(){
    document.getElementById("message").style.fontSize="20px";
    document.getElementById("message").style.visibility="visible";
    document.getElementById("message").style.width="100%";
    document.getElementById("message").style.height="100%";
    document.getElementById("message").style.padding="0px";
}

function fermer(){
    document.getElementById("message").style.fontSize="0px";
    document.getElementById("message").style.visibility="hidden";
    document.getElementById("message").style.width="0px";
    document.getElementById("message").style.height="0px";
}

